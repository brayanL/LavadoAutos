    private void cargarFacturas(){
        facturasModelTable.setRowCount(0);
        List<Factura> Factura = facturasDat.listarFacturas();
        if (Factura != null) {
            Factura.forEach((f) -> {   
                System.out.println("factura: "+f.getNumeroFactura()+", "+f.getDescuento());
                facturasModelTable.addRow(new Object[]{f.getNumeroFactura(), f.getFecha(),
                f.getCliente(),f.getTipoLavado(), f.getDescuento(), f.getTotal()
                });
            });
        }
    }

